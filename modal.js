// Показати модальне вікно
function showModal() {
    document.getElementById('myModal').style.display = 'block';
  }

  // Закрити модальне вікно
  function closeModal() {
    document.getElementById('myModal').style.display = 'none';
  }

  // Відкрити модальне вікно при кліку на кнопку
  function showForm() {
    showModal();
  }